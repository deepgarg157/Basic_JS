// Switch case syntax:-

let month="march"
switch (month) {
    case "jan":
        console.log("This is a jan month");
        break;

        case "feb":
        console.log("This is a feb month");
        break;

        case "march":
        console.log("This is a march month");
        break;

        case "april":
        console.log("This is a april month");
        break;

    default:
        console.log("Invalid month");
        break;
}